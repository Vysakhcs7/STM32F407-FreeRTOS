// Sender task: sends messages to the queue
void SenderTask(void *pvParameters) {
	char *message1 = "Hello from Sender 1!\r\n";
	char *message2 = "Hello from Sender 2!\r\n";
	uint32_t data = 120;

	while (1) {
		xQueueSend(messageQueue, &message1, portMAX_DELAY); // Send message 1
		vTaskDelay(pdMS_TO_TICKS(500));                    // Wait 1 second

		xQueueSend(messageQueue, &message2, portMAX_DELAY); // Send message 2
		vTaskDelay(pdMS_TO_TICKS(500));                    // Wait 1 second

		xQueueSend(numberQueue, &data, portMAX_DELAY); // Send message 1
		vTaskDelay(pdMS_TO_TICKS(500));                    // Wait 1 second
	}
}

// Receiver task: receives messages from the queue and prints them using printf
void ReceiverTask(void *pvParameters) {
	char *receivedMessage;
	uint32_t data;
	while (1) {
		if (xQueueReceive(messageQueue, &receivedMessage,
				portMAX_DELAY) == pdPASS) {
			printf("%s", receivedMessage);  // Print received message via printf
		}

		if (xQueueReceive(numberQueue, &data, portMAX_DELAY) == pdPASS) {
			printf("Received : %lu\r\n", data); // Print received message via printf
		}
	}
}

